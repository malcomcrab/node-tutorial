const os = require ('os')

// Platform 
console.log(os.platform())

//CPU Arch
console.log(os.arch())

//CPU Core  
console.log(os.cpus())

//Free Memory 
console.log(os.freemem())

//Total Memory 
console.log(os.totalmem())

//Home Directory
console.log(os.homedir())

//Uptime in Seconds
console.log(os.uptime())